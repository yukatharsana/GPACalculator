  public void loadReport()    
  {
      
        try {
            HashMap a = new HashMap();
            a.put("ID",txtf1.getText());
            
            jPanel1.removeAll();
            jPanel1.repaint();
            jPanel1.revalidate();
           
            JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\IIT19033\\Documents\\NetBeansProjects\\RAD_Project\\RAD_Project\\src\\Rad_project\\report.jrxml");
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            JasperPrint jprint= JasperFillManager.fillReport(jreport,a,conn);
              
            JRViewer v = new JRViewer(jprint);
            jPanel1.setLayout(new BorderLayout());
             jPanel1.add(v);
                
        
        } catch (JRException ex) {
            Logger.getLogger(Report1.class.getName()).log(Level.SEVERE, null, ex);
        }
      
      
  }
      
      
